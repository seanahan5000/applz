draw_number pha
            lsr
            lsr
            lsr
            lsr
            jsr draw_index_char
            pla
            and #$0f
            bpl draw_index_char ; always

draw_ascii_char
            cmp #"0"
            bcc :1
            cmp #"9"+1
            bcs :1
            sec
            sbc #"0"
            bpl draw_index_char  ; always

:1          cmp #"A"
            bcc :2
            cmp #"Z"+1
            bcs :2
            sec
            sbc #"A"-11
            bpl draw_index_char  ; always

:2          cmp #" "
            bne :3
            lda #10
            bne draw_index_char  ; always

:3          ldx #14
:4          cmp ascii_table,x
            beq :5
            dex
            bpl :4
            inx
:5          txa
            clc
            adc #37

draw_index_char
            ldy #>font
            sta temp
            asl
            asl
            asl
            bcc :1
            iny
:1          clc
            adc #<font
            bcc :2
            iny
:2          sec
            sbc temp
            sta char_mod+1
            bcs :3
            dey
:3          sty char_mod+2

draw_char   ldx #0
            lda ypos
            sta ycount
char_loop   ldy ycount
            lda hires_table_lo,y
            sta screenl
            lda hires_table_hi,y
            sta screenh
char_mod    lda font,x
            ldy xpos
            sta (screenl),y
            inc ycount
            inx
            cpx #7
            bne char_loop
            inc xpos
            rts

